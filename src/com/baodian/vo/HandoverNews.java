package com.baodian.vo;

public class HandoverNews {

	private String handover_time;
	private String handover_people;
	private String handover_status;
	private String remark;
	
	public String getHandover_time() {
		return handover_time;
	}
	public void setHandover_time(String handover_time) {
		this.handover_time = handover_time;
	}
	public String getHandover_people() {
		return handover_people;
	}
	public void setHandover_people(String handover_people) {
		this.handover_people = handover_people;
	}
	public String getHandover_status() {
		return handover_status;
	}
	public void setHandover_status(String handover_status) {
		this.handover_status = handover_status;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
}
